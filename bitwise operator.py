a = 10  
b = 4   

print("a =", a, " b =", b)

# Bitwise AND
print("a & b:", a & b)  

# Bitwise OR
print("a | b:", a | b) 

# Bitwise XOR
print("a ^ b:", a ^ b)  
 
# Bitwise NOT
print("~a:", ~a)        
print("~b:", ~b)        

# Bitwise Left Shift
print("a << 1:", a << 1) 

# Bitwise Right Shift
print("a >> 1:", a >> 1)
